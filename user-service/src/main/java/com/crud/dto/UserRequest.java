package com.crud.dto;

/**
 * DTO для запросов на создание или обновление пользователя.
 * Используется на входе в контроллер.
 * <p>
 * Содержит только те поля, которые пользователь может задать:
 * имя, email, возраст.
 * </p>
 * <p>
 * Реализован как record, что даёт неизменяемость, автоматический конструктор,
 * геттеры ({@code name()}, {@code email()}, {@code age()}) и методы {@code equals()}/{@code hashCode()}.
 * </p>
 *
 * @param name  имя пользователя (не должно быть пустым)
 * @param email электронная почта (уникальная, не пустая)
 * @param age   возраст (от 0 до 150)
 */
public record UserRequest(String name, String email, Integer age) {}